package javafinal;

public class PositiveNagitiveZero {
	public static void main(String[] args) 
	{
		
	int num= 9;
	
	if(num > 0)
	{
		System.out.println("Positive");
	}
	else if(num < 0)
	{
		System.out.println("Nagitive");
	}
	else
	{
		System.out.println("Zero");
	}

	}
	
	
}
